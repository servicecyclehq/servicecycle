#!/usr/bin/env node
'use strict';

/**
 * scripts/sync-ai-guide.js
 * ------------------------
 * Copies the canonical AI Guide from `docs/LapseIQ_AI_GUIDE.md`
 * (repo root) into `server/data/LapseIQ_AI_GUIDE.txt` so the docker
 * build context (which is `./server`) can include it.
 *
 * Why a sync script and not a symlink: Docker's COPY can't follow
 * symlinks out of the build context.
 *
 * Why .txt and not .md: `server/.dockerignore` excludes `*.md` so
 * stray markdown notes never bloat the runtime image. Renaming the
 * synced file to `.txt` sidesteps the exclusion without weakening
 * it. The route loads file contents — the extension doesn't matter
 * to the assistant.
 *
 * The canonical authoring location stays at `docs/LapseIQ_AI_GUIDE.md`
 * so humans can read it the same way as the rest of the docs/ tree.
 *
 * Usage:
 *   npm run guide:sync
 *
 * Run after every edit to docs/LapseIQ_AI_GUIDE.md and commit the
 * synced copy. CI does not regenerate this — it expects the synced
 * artifact to be in git so the docker build is reproducible.
 */

const fs = require('fs');
const path = require('path');

const REPO_ROOT  = path.resolve(__dirname, '..', '..');
const SRC_PATH   = path.join(REPO_ROOT, 'docs', 'LapseIQ_AI_GUIDE.md');
const DEST_DIR   = path.join(REPO_ROOT, 'server', 'data');
const DEST_PATH  = path.join(DEST_DIR, 'LapseIQ_AI_GUIDE.txt');

function main() {
  if (!fs.existsSync(SRC_PATH)) {
    throw new Error(`AI Guide missing at ${SRC_PATH}. Did the file move?`);
  }
  const body = fs.readFileSync(SRC_PATH, 'utf-8');
  if (body.length < 1000) {
    throw new Error(`AI Guide suspiciously small (${body.length} chars). Refusing to sync.`);
  }

  fs.mkdirSync(DEST_DIR, { recursive: true });
  fs.writeFileSync(DEST_PATH, body, 'utf-8');

  // Operator-facing sidecar so anyone poking around in server/data/ knows
  // the file is generated, not hand-edited.
  fs.writeFileSync(
    path.join(DEST_DIR, 'README.md'),
    [
      '# server/data',
      '',
      'Generated artifacts that ship inside the lapseiq-server docker image.',
      '',
      '## LapseIQ_AI_GUIDE.txt',
      '',
      'A copy of `docs/LapseIQ_AI_GUIDE.md`, synced via `npm run guide:sync`.',
      'Do NOT edit this file directly — edits will be overwritten by the next',
      'sync. Author the guide in `docs/LapseIQ_AI_GUIDE.md` and run the sync',
      'script before committing.',
      '',
      'The `.txt` extension is intentional: `server/.dockerignore` excludes',
      "`*.md` to keep stray notes out of the runtime image, and we don't",
      'want the AI Guide caught in that net.',
      '',
    ].join('\n')
  );

  console.log(`[guide:sync] copied ${body.length} chars from docs/LapseIQ_AI_GUIDE.md -> server/data/LapseIQ_AI_GUIDE.txt`);
}

main();
